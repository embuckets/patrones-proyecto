	private void creaCmbClientes() {
		ClienteDAO clienteDAO = new ClienteDAO();
		LinkedList<Cliente> lista = clienteDAO.listaClientes();
		Cliente clienteVacio = new Cliente(null, "Selecciona un Cliente", "", "");
		cmbClientes.addItem(clienteVacio);
		for (int i = 0; i < lista.size(); i++) {
			cmbClientes.addItem(lista.get(i));
		}
	}

	public void creaTablaEquipos() {

		pnlEquipos.setVisible(true);
		EquipoDAO equipoDAO = new EquipoDAO();
		LinkedList<Equipo> lista = equipoDAO.listaEquipos();
		Vector columnas = new Vector();
		columnas.add("Modelo");
		columnas.add("Color");
		columnas.add("Sistema Operativo");
		columnas.add("Precio");
		columnas.add("Fabricante");

		Vector filas = new Vector();
		Vector fila = new Vector();

		for (int i = 0; i < lista.size(); i++) {

			fila.add(lista.get(i).getModelo());
			fila.add(lista.get(i).getColor());
			fila.add(lista.get(i).getSistemaOperativo());
			fila.add(lista.get(i).getPrecio());
			fila.add(lista.get(i).getFabricante());

			filas.add(fila);
			fila = new Vector();
		}

		DefaultTableModel modelo = new DefaultTableModel(0, 0);
		modelo.setDataVector(filas, columnas);
		tblEquipos.setModel(modelo);
	}

	private void asignarEquipos() {
		Cliente cliente = (Cliente) cmbClientes.getSelectedItem();

		if (cliente.getId() != null) {
			int fila = tblEquipos.getSelectedRow();
			if (fila == -1) {
				JOptionPane.showMessageDialog(this, "Selecciona un Equipo", "Error!!!", JOptionPane.ERROR_MESSAGE);
			} else {
				String modelo = (String) tblEquipos.getValueAt(fila, 0);
				String color = (String) tblEquipos.getValueAt(fila, 1);
				Date myDate = new Date();
				String fecha = new SimpleDateFormat("yyyy-MM-dd").format(myDate);
				EquipoDAO equipoDAO = new EquipoDAO();
				EquipoCliente ec = equipoDAO.buscarEquipo(modelo, color); // se recibe el objeto incompleto
				ec.setIdCliente(cliente.getId()); // acompletado del objeto
				ec.setFecha(fecha); // acompletado del objeto

				if (equipoDAO.asignar(ec)) {
					JOptionPane.showMessageDialog(this, "Se agrego el equipo " + modelo + " con exito!!!",
							"AgregaciÃ³n de Equipos", JOptionPane.INFORMATION_MESSAGE);
				}

			}
		} else {
			JOptionPane.showMessageDialog(this, "Selecciona un Cliente", "Error!!!", JOptionPane.ERROR_MESSAGE);
		}

	}

